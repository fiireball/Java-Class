package dev.fiire;

public class Armor {

	private int armorValue;
	private String name;

	public int getArmorValue() {
		return armorValue;
	}

	public String getName() {
		return name;
	}

	public void setArmorValue(int armorValue) {
		this.armorValue = armorValue;
	}

	public void setName(String name) {
		this.name = name;
	}

}
